#include <iostream>


using namespace std;

int main()
{

    cout << "It's size of a boolean is:  " << sizeof(bool)<< " bytes" << endl;
    cout << "It's size of a character is:  " << sizeof(char)<< " bytes" << endl;
    cout << "It's size of a float is:  " << sizeof(float)<< " bytes" << endl;
    cout << "It's size of a short is:  " << sizeof(short)<< " bytes" << endl;
    cout << "It's size of a integer is:  " << sizeof(int)<< " bytes" << endl;
    cout << "It's size of a long is:  " << sizeof(long)<< " bytes" << endl;
    cout << "It's size of a double is:  " << sizeof(double)<< " bytes" << endl;

    return 0;
}
